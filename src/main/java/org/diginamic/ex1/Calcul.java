package org.diginamic.ex1;

/**
 * Classe utilitaire de calcul
 */
public class Calcul {

	/**
	 * Méthode statique permettant d'additionner deux entiers
	 * @param a entier a à additionner
	 * @param b entier b à additionner
	 * @return le résultat de la somme a+b
	 */
	public static int getAddition(int a, int b){
		return a+b;
	}

	/**
	 * Méthode statique permettant de soustraire deux entiers
	 * @param a entier a à soustraire
	 * @param b entier b à soustraire
	 * @return le résultat de la soustraction a-b
	 */
	public static int getSoustraction(int a, int b){
		return a-b;
	}
}
